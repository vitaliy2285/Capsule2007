select
  f.cell_number,
  f.nickname,
  f.memory_year,
  f.message,
  'published',
  107,
  now() - interval '30 days' - (random() * interval '500 days'),
  now() - interval '29 days' - (random() * interval '500 days'),
  now() - interval '30 days' - (random() * interval '500 days'),
  now() - interval '1 days'
from foundation f
where not exists (
  select 1
  from public.capsules c
  where c.cell_number = f.cell_number
    and (
      c.status in ('paid_pending_moderation', 'published')
      or (c.status = 'pending_payment' and (c.expires_at is null or c.expires_at > now()))
    )
);
